const N: usize = 10;
const STEPS: usize = 4;

fn main() {
    let mut dp = [0; N + 1];
    dp[N] = 1;
    let mut count = 0;

    // println!("{:?}", dp);

    for i in 0..N {
        // println!(" ---- {:?}", i);

        for j in 0..(N+1) {
            for k in 1..(STEPS+1) {
                if k > j {
                    break;
                }
                dp[j - k] += dp[j];
            }
            dp[j] = 0;
        }

        // println!("{:?}", dp);

        if i % 2 == 1 {
            count += dp[0];
        }
        // println!("{:?}", count);
    }
    // println!("{:?}", dp);
    println!("{:?}", count);
}

